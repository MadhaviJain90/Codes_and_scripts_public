#escape sequence characters
# it is the sequence of characters after the backslash \
# it comprises of more than one characters but represents one character when used within the strings
#  examples --- \n (newline), \t (tab), \' (single quote), \\ (backslash) etc

a = '''Speaker: \"Guten Tag, Madhavi. \n\tGern geschehen?\"\nMadhavi: \"Danke schon. \n\tMir geht's gut\"'''
print (a)

# will return
# Speaker: "Guten Tag, Madhavi. 
#         Gern geschehen?"
# Madhavi: "Danke schon.
#         Mir geht's gut"